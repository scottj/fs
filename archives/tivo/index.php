<h3 class="title"><a href="http://full-speed.org/archives/2003/12/01/tivo_cachecard.php">TiVo CacheCard</a></h3>

<p>I must confess:  I don&#8217;t have a TiVo.  But I&#8217;ve been wanting one for a while, but several complaints with the technology have kept me away.  Most of those have been resolved by third-party upgrades, such as larger hard drives.  However, I believe that my final complaint, the slow access to the database, has finally been resolved properly.</p>

<p>From what I&#8217;ve grasped from the forums, when you have a large database of recordings, it takes quite a while to access this database at times, especially when you are trying to view one of those recordings at the same time.  We&#8217;re dealing with IDE drives after all.  The solution to this problem for many has been to solder on additional RAM.  With this additional memory, the database is cached, and access becomes <strong>much</strong> faster.  I&#8217;ve never quite liked that idea, though, because it seems like the fastest way to either kill your TiVo our just completely void your warranty.</p>

<p>The <a href="http://www.9thtee.com/tivocachecard.htm" title="TiVo CacheCard" rel="nofollow">TiVo CacheCard</a> from 9th Tee makes this fix much simpler.  This card is plugged into the internal expansion slot on the TiVo.  The card provides one ethernet interface and one DIMM slot.  The user provides, say, a 512MB DIMM, and the TiVo CacheCard drivers go to work caching the database in that memory.</p>

<p>The ethernet is there because that&#8217;s what people normally use the expansion slot for.  This upgrade wouldn&#8217;t be practical to most without also including that.  And I see this as an added feature as well, since this would be combining two upgrade steps into one.</p>

<p>Anyhow, you can preorder these babies right now. They are said to be shipping mid-December.  [via <a href="http://www.pvrblog.com/pvr/2003/12/preorder_a_tivo.html" rel="nofollow">PVRblog</a>.]</p>



