<h3 class="title"><a href="http://full-speed.org/archives/2005/06/24/bittorrent_is_still_the_best.php">BitTorrent is Still the Best</a></h3>

<p><a href="http://www.livejournal.com/users/bramcohen/" rel="nofollow">Bram Cohen</a>, creator of BitTorrent, explained recently why Microsoft&#8217;s <a href="http://research.microsoft.com/~pablo/avalanche.htm" rel="nofollow">Avalanche</a> isn&#8217;t a BitTorrent-killer.  In his <a href="http://www.livejournal.com/users/bramcohen/20140.html" title="Avalanche" rel="nofollow">rather long post</a>, he makes many bold statements.  This is my favorite paragraph:<blockquote>&#8220;As you&#8217;ve probably figured out by now, I think that paper is complete garbage. Unfortunately it&#8217;s actually one of the better academic papers on BitTorrent, because it makes some attempt, however feeble, to do an apples to apples comparison. I&#8217;d comment on academic papers more, but generally they&#8217;re so bad that evaluating them does little more than go over epistemological problems with their methodology, and is honestly a waste of time.&#8221;</blockquote><br />
BitTorrent will not die any time soon.  And with the new <a href="http://www.bittorrent.com/trackerless.html" title="BitTorrent Goes Trackerless: Publishing with BitTorrent gets easier!" rel="nofollow">trackerless torrents</a>, it is only going to become stronger and faster.</p>



