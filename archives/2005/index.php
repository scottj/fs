<div class="blogbody">
<a name="001049"></a>
<h3 class="title"><a href="http://full-speed.org/archives/2005/12/14/uni-ball_jetstream.php">Uni-ball Jetstream</a></h3>

<p>I first found out about the <a href="http://www.amazon.com/exec/obidos/ASIN/B0004F7ILA/fullspeed-20" title="Uni-Ball Jetstream Ball Point Pen" rel="nofollow">Uni-Ball Jetstream Ball Point Pen</a> on <a href="http://www.kk.org/cooltools/archives/001051.php" title="Best pen for left handers" rel="nofollow">Cool Tools</a> last week.  It was claimed to be the &#8220;best pen for left handers&#8221;, so, being sort of a pen nut, I had to try it out.</p>

<p>I got a 2-pack at Target a couple of days ago, one black and one blue.  They had both the 0.7mm and 1.0mm varieties, but since I had just recently purchased a pack of 0.38mm pens, I decided to go big.  After years of using fine point pens, the 1mm seems sort of like the old jumbo pencils that I used way back in kindergarten.  The lines are big and bold.  The tip just seems to flow across the page.  It&#8217;s a really nice pen.</p>

<p>While I&#8217;m not quite on the <a href="http://www.dannygregory.com/2003/12/scratchy_pens.php" rel="nofollow">Danny</a> <a href="http://www.dannygregory.com/2004/03/pendemonium.php" rel="nofollow">Gregory</a> <a href="http://www.dannygregory.com/2005/11/pens_of_the_mom.php" rel="nofollow">level</a> of pen consumerism, I know a good pen the moment it hits my paper.  Next, I will probably need to hit the opposite end of the spectrum and try out the <a href="http://www.kk.org/cooltools/archives/001006.php" rel="nofollow">Uni-Ball Signo Bit</a> &#8212; a needle-like 0.18mm!</p>



