<h3 class="title">Bloglines Adds Publisher Tools</h3>
<p><!-- ckey="655BFDAD" -->Bloglines, which I stopped using long ago, recently added <a href="http://www.bloglines.com/profile?mode=4">support tools for publishers</a>.  This will be very useful if you ever have to move a feed and don&#8217;t want to rely on your 301 to handle everything for you.  But, if your feed stays in the same place when you move your site to a different domain, you are still out of luck.  Here&#8217;s hoping someone at Bloglines is once again answering customer emails.  I know that the last couple of emails that I sent in went unanswered.</p>

<p><b>Update</b>: No response from Bloglines, but I noticed that they did manually update my site URL.  Once this had been done, I was able to claim my feed and perform edits.</p>
