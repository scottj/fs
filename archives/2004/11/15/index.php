<h3 class="title"><a href="http://full-speed.org/archives/2004/11/15/halo_2.php">Halo 2</a></h3>

<p>I am rather surprised to have not seen much coverage of <a href="http://www.bungie.net/Games/Halo2/" rel="nofollow">Halo 2</a> on the blogging scene lately.  There&#8217;s been a lot of talk about the latest in the <acronym title="Grand Theft Auto">GTA</acronym> series and Half-Life 2 but no mention of Halo 2.</p>

<p>Perhaps it&#8217;s the Xbox-only nature of the game, but I read somewhere that there were over 2 million copies sold in the first week.  And I know for a fact that the bungie.net server stated that there were 385,000 people online while I was playing last night.  So why is everyone so quiet about this new game?</p>

<p>Well, I&#8217;m not going to stay quiet.  I may not be the best at the game, but here&#8217;s a link to <a href="http://www.bungie.net/Stats/PlayerStats.aspx?player=iNSANEcRAZY" title="Halo 2 stats for iNSANEcRAZY" rel="nofollow">my stats</a>.  It&#8217;s nice to see online console games using the web to allow the players to view their stats and the stats of their friends.  Heck, I even have an <em>RSS feed</em> from Bungie that is updated after every game I play.  That is really nice.</p>

<p>Anyhow, if you have an Xbox and haven&#8217;t checked out Halo 2 yet (is that really posssible?), please do so.  Sure, you&#8217;re adding money to Microsoft&#8217;s coffers, but that little company called Bungie that they bought is full of some really talented people.</p>

<p><b>Update:</b> <a href="http://www.wired.com/news/digiwood/0,1412,65732,00.html" title="Halo 2: Playing to the Bitter End" rel="nofollow">Wired News</a> gives an excellent brief on the new game.</p>



