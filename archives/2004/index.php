<p>A <a href="http://www.metafilter.com/mefi/32697" title="SpamAssassin: b0rked" rel="nofollow">recent post on MeFi</a> pointed me to some <a href="http://wiki.apache.org/spamassassin/CustomRulesets" title="CustomRulesets" rel="nofollow">custom rulesets</a> on the <a href="http://wiki.apache.org/spamassassin/FrontPage" rel="nofollow">SpamAssassin Wiki</a>.</p>

<p>My typical antispam arsenal consists of SpamAssassin plus Eudora&#8217;s Bayesian filtering.  On a normal day in the past, SpamAssassin (set to a threshold of 6.5) would return around twenty false negatives.  Of those twenty, Eudora might catch 75% on a good day.  The rest would be marked as spam in hopes of further training the Bayesian filter.</p>

<p>Since installing these new custom rulesets, however, only two spams have gotten past SpamAssassin.  Oddly enough, Eudora didn&#8217;t catch them either, but that&#8217;s beside the point.  Any reduction in spam volume means more time for doing other things, so I&#8217;m very pleased.</p>

<p>And since these rulesets often change frequently, there&#8217;s <a href="http://www.exit0.us/index.php/RulesDuJour" rel="nofollow">Rules Du Jour</a>, a <a href="http://www.gnu.org/software/bash/bash.html" rel="nofollow">bash</a> script that downloads the latest scripts automatically.</p>



