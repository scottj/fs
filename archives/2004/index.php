[ <a href="/archives/2004/09/">September</a> <a href="/archives/2004/09/08/"> 8</a>, <a href="/archives/2004/">2004</a> ]
</h2>


<div class="blogbody">
<a name="000872"></a>
<h3 class="title"><a href="http://full-speed.org/archives/2004/09/08/image_replacement.php">Image Replacement</a></h3>

<p>Using images to display text has long been the bane of web designers.  For starters, image tend to create an accessibility nightmare.  A person with impaired vision will often use a screen reader application to view a website, but to my knowledge, there is no software in this category that can &#8220;read&#8221; an image.  So in the interest of making websites acccessible to all, the web design community began seeking alternatives.</p>

<p>The most popular of these techniques, <a href="http://www.alistapart.com/articles/fir/" title="Facts and Opinion About Fahrner Image Replacement" rel="nofollow">Fahrner Image Replacement</a>, was <a href="http://rtnl.org.uk/words/19990709-site_updates.shtml" rel="nofollow">created in 1999</a> and <a href="http://www.stopdesign.com/also/articles/replace_text/" title="Using Background-Image to Replace Text" rel="nofollow">popularized by Doug Bowman</a> in 2003.  This technique, however, is not without its faults.  It has been proven to fail in many screen readers.  And just a year after Bowman&#8217;s tutorial on the technique was published, he <a href="http://www.stopdesign.com/log/2004/03/19/sxswpresentations.html#maincol" rel="nofollow">&#8220;officially deprecated&#8221;</a> the technique.</p>

<p>When this happened, the community was not extremely surprised.  After all, it had been known for some time that FIR wasn&#8217;t 100% compatible with screen readers.  But many were left wondering, &#8220;What next?&#8221;  We needed an alternative, but that alternative was not readily apparent.</p>

<p>Since those days, however, it seems that a lot of work has been done to find the perfect solution.  Dave Shea has a <a href="http://www.mezzoblue.com/tests/revised-image-replacement/" title="Revised Image Replacement" rel="nofollow">collection of examples</a> for various techniques.  Shaun Inman created the <a href="http://shauninman.com/mentary/past/ifr_an_fir_alternative.php" title="IFR" rel="nofollow">Inman Flash Replacement</a> technique.  And more recently, Inman collaborated with Mike Davidson and Tomas Jogin to create the <a href="http://www.mikeindustries.com/blog/archive/2004/08/sifr" title="Introducing sIFR: The Healthy Alternative to Browser Text" rel="nofollow">sIFR</a> technique.</p>

<p>I have yet to see a technique that is perfect.  Some don&#8217;t comply with the web standards.  Others don&#8217;t work with screen readers.  I suspect that the perfect technique will be a holy grail of sorts.  And until that technique comes along, use the technique that works best for your site.</p>



