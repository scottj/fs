The problem is that spammers typically don&#8217;t care about these lawsuits and have started including this haiku in their messages.  After reading Nelson&#8217;s post, I examined the spams that had mad it past SpamAssassin, and he was right&#8212;I saw Habeas all over the place.  <a href="http://log.does-not-exist.org/archives/2004/01/12/1129_habeas_spam.html" rel="nofollow">Many</a>  <a href="http://www.uncledirtae.com/blog/archives/2004/01/12/habeas_spam.php" rel="nofollow">others</a> <a href="http://www.ro0t.com/archives/000103.php" rel="nofollow">have</a> <a href="http://www.mspland.com/weblog/computer/HabeasSpam.html" rel="nofollow">also</a> <a href="http://stevenf.com/mt/archives/000552.php" rel="nofollow">noticed</a> <a href="http://www.neilturner.me.uk/2004/Mar/10/habeas_being_exploited_again.html" rel="nofollow">this</a> <a href="http://www.jacobsen.no/anders/blog/archives/2004/03/09/how_the_habeas_haiku_is_failing.html" rel="nofollow">problem</a>.

<p>SpamAssassin gives Habeas a -8 score by default.  This is a fair score to give an effective rule, but this rule is obviously failing.  So while Nelson recommends giving Habeas a score of 0 in SpamAssassin, effectively removing the rule, I&#8217;m giving it a 5, penalizing those who use this haiku:  <code>score HABEAS_SWE 5</code>.  [via <a href="http://www.nelson.monkey.org/~nelson/weblog/tech/bad/habeasMark.html" title="Habeas haiku considered harmful" rel="nofollow">Nelson Minar</a>]</p>



