<h2 class="date">[ December 06, 2004 ]</h2>
<div class="blogbody">
<h3 class="title"></h3>
<p><a href="http://wifinetnews.com/">Wi-Fi Networking News</a>, always a trusted source for news about wireless networking, released two interesting bits of news last week.</p>

<p>First, <a href="http://wifinetnews.com/archives/004518.html" title="Gigabit Virtual Fiber in Lower Manhattan">GigaBeam is planning multi-gigabit wireless networking in Lower Manhattan</a>.  They are calling this &#8220;virtual fiber&#8221;.  It will be using point-to-point technology operating in the 71-76 GHz and 81-86 GHz ranges.  No prices have been announced yet, but I sure would love to have something like this in my apartment.</p>

<p>And second, <a href="http://wifinetnews.com/archives/004520.html" title="802.11n Devices by mid-2006">an update on the progress of 802.11n</a>.  The short of this is that we should expect to see 802.11n devices by mid-2006.  For those of you not familiar with <a href="http://en.wikipedia.org/wiki/802.11#802.11n">802.11n</a>, this is the next generation in wireless networking, promising real throughput of at least 100Mbps.</p>

<p>Exciting things are happening in the wireless world.  Within a couple of years, it will be a totally different landscape.</p>
