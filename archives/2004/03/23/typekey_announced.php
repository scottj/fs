<h3 class="title">TypeKey Announced</h3>
<p><a href="http://www.sixapart.com/">Six Apart</a> has <a href="http://www.sixapart.com/log/2004/03/movable_type_30.shtml">announced</a> <a href="http://www.typekey.com/">TypeKey</a>.  Due in part to many <a href="http://diveintomark.org/archives/2004/03/22/you-blow-me">complaints</a>, they have also put out a <a href="http://www.sixapart.com/log/2004/03/you_wanted_the_.shtml">FAQ</a>.  Burningbird has excellent analyses from both <a href="http://weblog.burningbird.net/fires/technology/typekey_the_patriot_act_of_weblogging.htm">before</a> and <a href="http://weblog.burningbird.net/fires/sensuoustechnology/typekey_final_act.htm">after</a> the FAQ release.</p>

<p>While it&#8217;s great to see the creators of <a href="http://www.movabletype.org/">Movable Type</a> working on <a href="http://joi.ito.com/archives/2004/03/21/six_apart_announced_typekey.html">new technology</a>, it seems that most of their users are <a href="http://golem.ph.utexas.edu/~distler/blog/archives/000335.html#c000826">not happy</a> with the proposal of a centralized service for authenticating commentors.</p>
