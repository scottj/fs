<h3 class="title"><a href="http://full-speed.org/archives/2004/04/24/hdtv_tivo_released.php">HDTV TiVO Released</a></h3>

<p>After months of waiting, TiVo has <a title="HD TiVOs Begin Shipping" href="http://www.extremetech.com/article2/0,1558,1572712,00.asp?kc=ETRSS02129TX1K0000532" rel="nofollow">finally released</a> its first HD DVR.  The <a title="First HDTV TiVo is out" href="http://www.engadget.com/entry/1769989454587533/" rel="nofollow">Hughes HR10-250 DirecTiVo</a> features dual HD and SD tuners, a 250GB hard drive, and support for recording locally aired DTV broadcasts.  It can record up to 30 hours of HD and up to 200 hours of standard definition programming.  Much more information about this new device can be found on the <a href="http://www.tivocommunity.com/tivo-vb/showthread.php?s=9e6d38decc93689cf8740cea93616663&amp;threadid=151443" rel="nofollow">TiVo Community forums</a> along with some <a href="http://www.tivocommunity.com/tivo-vb/showthread.php?s=9e6d38decc93689cf8740cea93616663&amp;threadid=171422" rel="nofollow">first impressions</a>.</p>

<p><b>Update:</b> Matt Haughey over at <a href="http://www.pvrblog.com/pvr/2004/04/hd_tivos_begin_.html" title="HD TiVOs Begin Shipping" rel="nofollow">PVRblog</a> is predicting that we&#8217;ll be seeing these for $400-500 on the street soon.  Also, he is expecting a review unit, so I&#8217;m sure we will see an excellent review on his site before long.</p>



