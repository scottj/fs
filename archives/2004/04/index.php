<h3 class="title"><a href="http://full-speed.org/archives/2004/04/10/mesothelioma.php">Mesothelioma?</a></h3>

<p><a href="http://battellemedia.com/archives/000562.php" title="I'll Take Mesothelioma for $100" rel="nofollow">John Battelle</a> found a story about <a href="http://washingtontimes.com/upi-breaking/20040408-095925-6844r.htm" rel="nofollow">the bidding war over the keyword Mesothelioma</a> for targeted search engine advertising.  It seems the lawyers are raking in so much money on their Mesothelioma cases that they have bid the keyword up to $100 in some cases.</p>

<p>According to <a href="http://www.mesotheliomaweb.org/" rel="nofollow">Mesothelioma Web</a>, one could have been exposed to asbestos for a period of only a few months and then develop the rare cancer 30 to 40 years later. So, many people who were exposed in the mid-1900s are just now seeing symptoms.</p>

<p>And the attorneys are loving it.  They have often given themselves new titles, such as &#8220;Asbestos Attorney&#8221; or &#8220;Mesothelioma Lawyer&#8221;.  They are building up an entire segment of their industry around this deadly disease and its victims.</p>

<p>And in the world of always-on internet connections that we live in today, it is only natural to see an overwhelming amount of advertising for anything that can be so profitable.  This make me wonder, however, if there are other keywords that are so costly.</p>



