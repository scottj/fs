<h3 class="title">iTunes Music Store RSS</h3>
<p>Apple now has an <a href="http://phobos.apple.com/WebObjects/MZSearch.woa/wa/MRSS/rssGenerator" title="iTunes Music Store RSS Generator">RSS generator</a> for the iTunes Music Store.  You can get a feed for just about anything you want.  <a href="http://ax.phobos.apple.com.edgesuite.net/WebObjects/MZStore.woa/wpa/MRSS/newreleases/genre=0000800/limit=10/rss.xml">Latest 10 Hip Hop/Rap Releases</a>.  <a href="http://ax.phobos.apple.com.edgesuite.net/WebObjects/MZStore.woa/wpa/MRSS/topsongs/genre=0008200/limit=50/rss.xml">Top 50 Pop &amp; Alternative Songs</a>.  The possibilities are limitless.</p>

<p><b>Update:</b>  It seems that <a href="http://www.metafilter.com/mefi/30910">several</a> <a href="http://www.macminute.com/2004/01/22/itunesrss">other</a> <a href="http://www.macdailynews.com/comments.php?id=P1993_0_1_0">sites</a> have picked up on this one now, too.  This service is sure to be a hit.</p>
