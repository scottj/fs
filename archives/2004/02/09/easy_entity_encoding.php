<h3 class="title">Easy Entity Encoding</h3>
<p>Dan Cederholm wrote a <a href="http://www.simplebits.com/archives/2004/02/05/simplecode.html"  title="SimpleCode">simple utility</a> for converting XHTML (or other code-like data) into entity-encoded markup.  This is a simple <a href="http://www.simplebits.com/cgi-bin/simplecode.pl">perl script</a> that&#8217;s just designed to save a bit of time when writing web content.</p>

<p><a href="http://waffle.wootest.net/">Jesper</a> rewrote the script in a neat, self-contained html page with a bit of JavaScript.  <a href="http://wootest.net/simplecode.htm">Jesper&#8217;s version</a>, <a href="/misc/simplecode/">mirrored here</a>, provides live previews and requires no server interaction.  Very slick.  A little more time saved.</p>

<p>This looks a lot like Jon Hicks&#8217; <a href="http://www.hicksdesign.co.uk/journal/archives/000317.php">live comment previews</a>.  There&#8217;s just something great about seeing my input rendered by the browser as I type.  It&#8217;s almost like having a WYSIWYG word processor in the browser.  We need more of this kind of stuff on the web.</p>
