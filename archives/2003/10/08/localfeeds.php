<h3 class="title">Localfeeds</h3>
<p>I finally got around to installing some <a href="http://awstats.sourceforge.net/" title="AWStats">stats analysis software</a> on this site yesterday, and the results have been interesting.  What has intrigued me the most, however, is <a href="http://austin.localfeeds.com/" title="Localfeeds:Austin, TX, USA">Localfeeds</a>.  I was pleasantly surprised to find, via my web server&#8217;s logfiles, that my entries from this site are featured on the Localfeeds front page for my area.  Much like <a href="http://geourl.org/" title="GeoURL">GeoURL</a>, they geographically group websites.  But unlike GeoURL, they also will <a href="http://www.karchner.com/update/archives/000304.html">give you an RSS feed</a> of these groups.  I&#8217;m adding my area to my newsreader right now.</p>
