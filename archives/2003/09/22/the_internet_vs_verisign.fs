<h3 class="title">The Internet vs. VeriSign</h3>

<p>So we have a workaround for VeriSign&#8217;s nasty <acronym title="Domain Name System">DNS</acronym> changes, but that&#8217;s not enough.  Popular Enterprises, LLC, <a href="http://www.wired.com/news/business/0,1367,60518,00.html" title="Wired News: VeriSign Sued Over Search Service">sued VeriSign</a> over the new Site Finder &#8220;service&#8221;.</p>

<p>Next, <acronym title="The Internet Corporation for Assigned Names and Numbers"><a href="http://www.icann.org/">ICANN</a></acronym> announced that they have called upon VeriSign to voluntarily suspend the service.  Clicking on <a href="http://this-is-not-a-real-domain.com/">a fake url</a> shows that VeriSign has not complied.  Will this ordeal ever end?</p>

