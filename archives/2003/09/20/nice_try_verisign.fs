<h3 class="title">Nice Try, VeriSign</h3>

<p>In response to &#8220;high demand&#8221; from users, <abbr title="Internet Software Consortium">ISC</abbr>, the organization that develops and maintains <acronym title="Berkeley Internet Name Daemon">BIND</acronym>, has issued a <a href="http://www.isc.org/products/BIND/delegation-only.html" title="Internet Software Consortium - BIND">delegation-only patch</a> that prevents VeriSign&#8217;s <a href="http://www.wired.com/news/business/0,1367,60466,00.html" title="VeriSign Takes Sting out of Typos">hostile takeover</a> of the .com and .net domains.  In order for this effort to be successful, we must all <a href="http://marc.theaimsgroup.com/?l=bind-users&amp;m=106378709918676&amp;w=2" title="BIND 9.2.2-P1 is now available.">update our nameservers</a> with this patch and add a few lines to the name server configuration.  Will this kill the &#8220;Site Finder&#8221;?  Only time will tell.</p>

