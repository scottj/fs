<h3 class="title">Athlon 64 is Finally Here</h3>

<p>The <a href="http://www.amd.com/us-en/Corporate/VirtualPressRoom/0,,51_104_543~74465,00.html" title="AMD Ushers in Era of Cinematic Computing with the AMD Athlon 64 FX Processor">announcement</a> came as no surprise today.  We&#8217;ve been anticipating this one for many months now.  The <a href="http://www.amd.com/us-en/Processors/ProductInformation/0,,30_118_9484,00.html" title="AMD Athlon 64">Athlon 64</a> processor family, including the beefy <a href="http://www.amd.com/us-en/Processors/ProductInformation/0,,30_118_9485_9488,00.html" title="AMD Athlon 64 FX Processor">64FX</a>, is finally here, along with a <a href="http://www.internetnews.com/ent-news/article.php/3081201" title="Microsoft Betas Windows XP 64-Bit on AMD64">beta release</a> of 64-bit Windows.  And <a href="http://rss.com.com/2009-1006_3-5080659.html?tag=fd_lede" title="AMD's 64-bit adventure begins | CNET News.com">news</a> <a href="http://www.infoworld.com/article/03/09/23/HNxpbeta_1.html?source=rss&amp;url=http://www.infoworld.com/article/03/09/23/HNxpbeta_1.html" title="InfoWorld: Microsoft unwraps Windows XP 64-bit beta: September 23, 2003: By : Applications">coverage</a> <a href="http://slashdot.org/article.pl?sid=03/09/23/1619201" title="Slashdot | Athlon 64 Debuts">abounds</a>.</p>

