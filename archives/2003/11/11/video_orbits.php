<h3 class="title">Video Orbits</h3>
<p>I thought that I&#8217;d lost <a href="http://hi.eecg.toronto.edu/orbits/orbits.html" title="High-resolution stills and environment maps from video ...">the link to this site</a> a few days ago.  While planning for my upcoming trip to Europe, I realized that it would be nice to have software on hand to make some image composites.  This software is probably the best that I&#8217;ve seen for what I wish to accomplish.  But I had no idea what it was called or who wrote it.  Now I do, however:  <a href="http://comparametric.sourceforge.net/" title="The Comparametric Toolkit Page">Comparametric</a> by <a href="http://wearcam.org/" title="WearCam.org">Professor Steve Man</a> of the University of Toronto.  Google is a wonderful tool when the brain forgets.</p>
