<h3 class="title">Wired Diamonds</h3>
<p>I just got the Semtember issue of <a href="http://www.wired.com/" title="Wired">Wired</a> in the mail a couple of days ago.  She cover features a woman adorned in nothing but diamonds.  The story behind this photo is called <a href="http://www.wired.com/wired/archive/11.09/diamond.html" title="The New Diamond Age">&#8220;The New Diamond Age&#8221;</a>.  This is a very interesting read.</p>

<p>We all know that <a href="http://www.debeers.com/" title="De Beers">De Beers</a> is the <a href="http://www.dashes.com/anil/index.php?archives/004817.php" title="diamonds are for never">only reason</a> that diamonds cost so much.  Any many of us know of the evil practices that this company engages in.  After reading these articles on diamonds, you may find yourself reconsidering the idea of spending such a large sum of money on such a worthless rock.</p>

<p>Remember, De Beers recommends that you spend 16.67% of your annual salary on an engagement ring.  For many of us, that amount would feed quite a few hungry people for a year.  I think I&#8217;ll wait for these cheaper, lab-grown stones.</p>
