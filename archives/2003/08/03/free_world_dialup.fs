<h3 class="title">Free World Dialup</h3>

<p>I just found out about <a href="http://www.fwdnet.net/" title="Free World Dialup">Free World Dialup</a> today.  It looks like an interesting free <acronym title="Voice over Internet Protocol">VoIP</acronym> service.  I signed on using a version of  <a href="http://www.xten.com/" title="Xten">Xten&#8217;s</a> <a href="http://www.xten.com/lite.php" title="X-Lite">X-Lite</a> that was customized for FWD.  I&#8217;ve only made one call to a time service thus far, but the quality was great.  They also have a number you can dial for an echo service.  This seems like a good idea for those curious about the delays that such networks have.  I&#8217;ll certainly have to try this out next.</p>

<p>A strange thing happened tonight, though.  Soon after I made a couple of test calls, someone by the name of &#8220;Rodrigo Rodrigues&#8221; called me.  This is strange because I requested that me number be unlisted.  I&#8217;m guessing that I may have just been a victim of random dialing, but I guess I&#8217;ll never know.</p>

