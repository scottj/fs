<h3 class="title">Notebook or Handheld PC?</h3>
<p>So I was reading PC Magazine a few days ago (free subscribtion&#8211;I&#8217;d never pay for that one), and I stubmled across an ad for the <a href="http://www.necsam.com/MP6" title="NEC MobilePro 900">most recent addition</a> to the NEC MobilePro series of <a href="http://www.microsoft.com/catalog/display.asp?site=10617&amp;subid=22&amp;pg=4" title="Microsoft Windows Powered Handheld PC">Handheld PCs</a>.  This one is listed at <a href="http://www.cdw.com/shop/products/default.asp?edc=487401">$899</a>.  The form factor of this PDA on steroids is certainly <a href="http://www.necsolutions-am.com/mobilesolutions/products/MobilePro/900/specs.htm" title="MobilePro 900 Specs">impressive</a>.  And while I like the wide-format display, 640x240 isn&#8217;t much real estate, especially when I&#8217;m stuggling to see a passive matrix screen.</p>

<p>So why would anybody want to buy this when you can get a <a href="http://info.lindows.com/mobilepc/mobilepc.htm">Lindows Mobile PC</a> for only $799?  This thing has a 1024x768 <acronym title="Thin Film Transistor">TFT</acronym> display.  That&#8217;s much more usable to me.</p>
