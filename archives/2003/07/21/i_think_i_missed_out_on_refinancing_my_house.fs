<h3 class="title">I think I missed out on refinancing my house</h3>

<p>OK, so I was thinking about refinancing my house a while back.  I bought it in 2001, just before the <a href="http://news.com.com/2009-1017_3-253125.html" title="Assessing the carnage">tech bubble bursted</a>.  A couple of months ago, I had my <a href="http://www.juliechadwick.com/" title="Julie">girlfriend</a> (a REALTOR&reg;) contact the president of a <a href="http://www.capital-mortgage.net/" title="Capital Mortgage Services">mortgage company</a> that she had been working with.  This guy promised that he could have our house refinanced within two weeks and that he would call us by the end of the following day.  Well, first off, he never called back.  We had to call him.  And when we did, his responses to our questions were rather curt.  He made promise after promise but never came through with anything.  And so I didn&#8217;t get the great rate cut that I was hoping for.</p>

<p>Now, three months later, MSNBC seems to be saying that <a href="http://www.msnbc.com/news/937692.asp" title="Did you miss the refinancing boat?">I&#8217;ve waited too long</a>.  The recent <a href="http://money.cnn.com/2003/07/10/pf/yourhome/q_weekly_rates/index.htm" title="Mortgage rates surge again">media coverage</a> along with my GF&#8217;s local market research confirm that the mortgage rates are skyrocketing.</p>

<p>Now I won&#8217;t lie:  I got a pretty good rate on the loan in 2001.  But earlier this year, mortgage rates hit 40-year lows.  Let&#8217;s just say that I could have easily shaved about two and a half points off of my rate if the re-fi came through.</p>

<p>Nothing has happened since the initial failed attempt at getting my house refinanced.  Did I miss out on some huge savings?</p>

