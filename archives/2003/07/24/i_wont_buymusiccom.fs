<h3 class="title">I won't BuyMusic.com!</h3>

<p>Jeffrey Zeldman beat me to <a href="http://www.zeldman.com/daily/0703b.shtml#outoftune" title="Buymusic.com: out of tune with the times">the punch</a> with his bit about <a href="http://www.buy.com/">Buy.com</a>&#8217;s new music site:</p>

<blockquote>The Web Standards Project <a href="http://www.webstandards.org/buzz/archive/2003_07.html#a000169" title="Some men you just can't reach">says everything</a> we would have said about this foolishness.</blockquote>

<a name="more"></a>
<p>Updated July 27, 2003:</p>

<p>Apple&#8217;s <a href="http://www.apple.com/music/store/">iTunes Music Store</a> certainly does it right.  They sold a very large amount of music in the first few weeks, and I don&#8217;t see any sign of that slowing down.  And with the ability to play that music on the <a href="http://www.apple.com/ipod/" title="iPod">ultimate personal music player</a>, the momentum is certainly powerful.</p>

<p>But BuyMusic.com music won&#8217;t play on my iPod.  In fact, I can&#8217;t even download it on my Mac.  It&#8217;s not only restricted to windows, but it&#8217;s also restricted to Internet Explorer and WMA audio.  The will certainly be competitors that will challenge Apple in this market, but BuyMusic.com is not one of them.</p>

