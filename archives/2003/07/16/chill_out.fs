<h3 class="title">Chill Out</h3>

<p>In these days of constant chaos, I think that all of the <a href="http://www.buymeds.com/affiliates/aw.asp?A=3359&amp;Task=Click&amp;B=63&amp;P=14" title="Viagra">Viagra</a> spam its misdirected.  These spammers would be doing a much greater service to the world if they would simply recommend <a href="http://www.buymeds.com/affiliates/aw.asp?A=3359&amp;Task=Click&amp;B=68&amp;P=18" title="Xanax">Xanax</a> or <a href="http://www.buymeds.com/affiliates/aw.asp?A=3359&amp;Task=Click&amp;B=60&amp;P=23" title="Valium">Valium</a>.  I think the world would be a better place if people would take some chill pills.  Can you imagine a world filled with people who are just <em>that much more</em> relaxed?</p>

