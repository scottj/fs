<h3 class="title">Fun with MovableType</h3>

<p>Until my first <a href="http://www.movabletype.org/" title="MovableType">MT</a> install last week, I had always thought that it was just a simple tool for blogging.  I&#8217;m been learning rather quickly, however, that it&#8217;s for more than that.  <a href="http://www.bradchoate.com/past/001656.php" title="Doing your whole site with MT">Brad Choate</a> runs his whole site on MT.  <a href="http://a.wholelottanothing.org/features.blah/entry/007162" title="Beyond the Blog">Matt Haughey</a> has done the same on many sites.   In fact, I&#8217;m find <a href="http://scott.typepad.com/delimiter/2003/07/relative_paths_.html" title="Relative Paths in MT">references</a> of this type of thing happening all over the place.  I know that my site is currently missing some important info.  Right now it&#8217;s just a blog needing some personalization.  These new tips will certainly make life easier when I finally getting around to fixing things up.</p>

