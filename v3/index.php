	<h3 class="title"><a href="http://full-speed.org/archives/2006/03/20/microsoft_and_http.php">Microsoft and HTTP</a></h3>
	
	<p>Why is it that when you <a href="http://www.google.com/search?q=http">search Google for &#8220;http&#8221;</a>, Microsoft is the first result?  I first stumbled upon this when clicking on a malformed URL in Firefox.  <a href="http://http://full-speed.org/">Try it for yourself</a>.  Since Firefox fires off a Google search on URLs it doesn&#8217;t understand, every HTTP URL that doubles up on the &#8220;http://&#8221; part is sent to Microsoft by Firefox.  This just seems wrong on some level.</p>
	
	
	
